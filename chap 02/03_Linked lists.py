# Linked Lists : Collection of data elements, called nodes, So this is sort of like an array, but a little bit different
# The first item in the list is called the head
# Each element has a field that refers to the next item in the list.
# The last item in the list has a field that points to nothing, which indicates that it is the end of the list
# Linked lists provide a benefit over regular arrays in that it's fast and easy to add and remove items from the list.
# we can insert a new item into the list by placing it at the head
